// 生成common.json文件内容
function generateCommonFile(lang, data) {
  return JSON.stringify(data, null, 2);
}

// 主函数
async function generateCommonFiles() {
  console.log('🚀 开始生成公共语言文件...\n');

  const localesDir = path.join(__dirname, '../src/locales');
  
  for (const lang of SUPPORTED_LANGUAGES) {
    try {
      const langDir = path.join(localesDir, lang);
      const commonFile = path.join(langDir, 'common.json');
      
      // 确保语言目录存在
      if (!fs.existsSync(langDir)) {
        fs.mkdirSync(langDir, { recursive: true });
      }
      
      // 获取翻译数据（如果没有特定语言的翻译，使用英文作为默认值）
      const data = translations[lang] || translations['en'];
      
      // 生成文件内容
      const content = generateCommonFile(lang, data);
      
      // 写入文件
      fs.writeFileSync(commonFile, content, 'utf8');
      
      console.log(`✅ 已生成: ${lang}/common.json`);
    } catch (error) {
      console.error(`❌ 生成 ${lang}/common.json 失败:`, error.message);
    }
  }
  
  console.log('\n🎉 公共语言文件生成完成！');
  console.log(`📁 共生成 ${SUPPORTED_LANGUAGES.length} 个语言文件`);
}

// 运行脚本
generateCommonFiles().catch(console.error); 